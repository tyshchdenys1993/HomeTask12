package com.aqacourses.cucumber.utils;

public class YamlFile {
    private String email;
    private String password;
    private String url;

    /**
     * Get email
     *
     * @return email
     */
    public String getEmail() {
        return email;
    }

    /**
     * Get password
     *
     * @return password
     */
    public String getPassword() {
        return password;
    }

    /**
     * Get URL
     *
     * @return url
     */
    public String getURL() {
        return url;
    }
}
